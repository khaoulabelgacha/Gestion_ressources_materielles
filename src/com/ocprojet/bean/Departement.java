package com.ocprojet.bean;

public class Departement {
       public int idDep;
       public String nom;
       public String chefDef;
	public int getIdDep() {
		return idDep;
	}
	public void setIdDep(int idDep) {
		this.idDep = idDep;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getChefDef() {
		return chefDef;
	}
	public void setChefDef(String chefDef) {
		this.chefDef = chefDef;
	}
       
       
}
